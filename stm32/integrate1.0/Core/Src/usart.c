/*
* @brief 小电脑数据具体解析函数
* @param None
* @retval None
*/
int data_count;
void PCData()
{
	
	pcdata[data_count] = pc_rx_buf;
	data_count++;
	if(pcdata[0] != 0x01)
	{
		data_count = 0;
		return;
	}	
  if(data_count < 8) return;
	if(pcdata[7] != 0x01)
	{
		data_count = 0;
		return;
	}  

	//memcpy(&cmd.line,pcdata,sizeof(int16_t));
  	cmd.line=pcdata[2] | (pcdata[1] << 8);
 	cmd.rotate=pcdata[4] | (pcdata[3]<<8);
  	cmd.rotatespeed=pcdata[6] | (pcdata[5]<<8);
	data_count = 0;
}


